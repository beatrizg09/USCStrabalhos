    private void jBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterarActionPerformed
        
        if(jTvenderores.getSelectedRow() != -1){
            
           tableModel.setValueAt(txtnome.getText(),jTvenderores.getSelectedRow(), 0);
           tableModel.setValueAt(txtcpf.getText(),jTvenderores.getSelectedRow(), 1); 
           tableModel.setValueAt(txtelefone.getText(),jTvenderores.getSelectedRow(), 2);
           tableModel.setValueAt(txtid.getText(),jTvenderores.getSelectedRow(), 3);
        }
        
        
    }//GEN-LAST:event_jBalterarActionPerformed

    private void jBsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarActionPerformed
       
        Vendedor2 v = new Vendedor2();
        v.setNome(txtnome.getText());
        v.setCpf(txtcpf.getText());
        v.setTelefone(txtelefone.getText());
        v.setID(Integer.parseInt(txtid.getText()));
        
        tableModel.addRow(v);
        
        
        
        
    }//GEN-LAST:event_jBsalvarActionPerformed

